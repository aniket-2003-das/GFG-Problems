//User function Template for C++
class Solution
{
public:
    int trailingZeroes(int N)
    {
        int n=N;
        int res=0;
        while(n>0){
            res +=(n/5);
            n/=5;
        }
        return res;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans  = ob.trailingZeroes(N);
        cout<<ans<<endl;
    }
    return 0;
}
